    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        boolean delete = false;
        if (JOptionPane.showConfirmDialog(this, "Apakah anda akan merubah data \n" + " \"" + nama + "\"", "Konfirmasi", JOptionPane.YES_NO_OPTION) == 0) {
            try {
                delete = karyawanCtrl.deleteKaryawan(Integer.valueOf(uid));
            } catch (SQLException | IOException | PropertyVetoException ex) {
                Logger.getLogger(view.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (delete == true) {
                try {
                    JOptionPane.showMessageDialog(this, "Data \"" + nama + "\" Berhasil Dihapus", "Info", 1);
                    hide3Button();
                    fillTable();
                } catch (IOException | PropertyVetoException ex) {
                    Logger.getLogger(view.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

