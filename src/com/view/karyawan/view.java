    private void initTable() {
        table tbl = new table();
        String kolom[] = {"No", "NIK", "Nama Karyawan", "No Telp / HP", "alamat"};
        boolean edit[] = {false, false, false, false, false};
        model = tbl.tabel(kolom, edit);
        tblKaryawan.setModel(model);
        tblKaryawan.getColumnModel().getColumn(0).setMaxWidth(45);
        tblKaryawan.getColumnModel().getColumn(1).setMaxWidth(225);
        tblKaryawan.setShowGrid(true);
    }

