    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(this, "Apakah anda akan merubah data \n" + " \"" + nama + "\"", "Konfirmasi", JOptionPane.YES_NO_OPTION) == 0) {
            try {
                if (!editKaryawan.isVisible()) {
                    MainForm.desktopPane.add(editKaryawan);
                    editKaryawan.setVisible(true);
                    edit.uid = Integer.valueOf(uid);
                    edit.txtNik.setText(nik);
                    edit.txtNama.setText(nama);
                    edit.txtTelp.setText(telp);
                    edit.txtAlamat.setText(alamat);
                }
            } catch (Exception ak) {
                editKaryawan = new edit();
                MainForm.desktopPane.add(editKaryawan);
                editKaryawan.setVisible(true);
                edit.uid = Integer.valueOf(uid);
                edit.txtNik.setText(nik);
                edit.txtNama.setText(nama);
                edit.txtTelp.setText(telp);
                edit.txtAlamat.setText(alamat);
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

